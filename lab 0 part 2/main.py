import random

randomNumber = random.randint(1, 6)
if randomNumber == 1:
    print("Roll of dice is 1")
elif randomNumber == 2:
    print("Roll of dice is 2")
elif randomNumber == 3:
    print("Roll of dice is 3")
elif randomNumber == 4:
    print("Roll of dice is 4")
elif randomNumber == 5:
    print("Roll of dice is 5")
else:
    print("Roll of dice is 6")